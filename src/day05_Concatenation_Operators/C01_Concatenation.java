package day05_Concatenation_Operators;

public class C01_Concatenation {
    public static void main(String[] args) {

        System.out.println("25" + "34"); // 2534

        System.out.println("Java" + 2 + 7); // Java27

        System.out.println(2+7 + "Java"); // 9Java

        System.out.println("Java" + 2 * 7); // Java14

        System.out.println(""+ 5 + 3); //53

    }
}
